    private void btnoBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnoBuscarActionPerformed
        String Id = cajaid.getText();

        Informes info = GestionInformes.buscarRegistro(Id);
        if (info == null) {
            JOptionPane.showMessageDialog(null, ERRORBUSCAR);

        } else {
            cajaIdFruta.setText(info.getId_Fruta());
            cajaAsunto.setText(info.getAsunto());
            cajaDescripcion.setText(info.getDescripcion());
        }
    }//GEN-LAST:event_btnoBuscarActionPerformed

