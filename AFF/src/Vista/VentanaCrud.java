    private void btnoIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnoIngresarActionPerformed
        boolean datosvalidos = true;
        String codigo = cajaPerito.getText();
        String Fruta = cajaIdFruta.getText();
        String Asunto = cajaAsunto.getText();
        String descr = cajaDescripcion.getText();
        System.out.println("la fruta devuelve " + Fruta);
        if ("".equals(Fruta) || "".equals(Asunto) || "".equals(descr)) {
            datosvalidos = false;
        }

        if (datosvalidos == true) {

            if (GestionInformes.insertar(codigo, Fruta, Asunto, descr) == true) {
                JOptionPane.showMessageDialog(null, MensajeInsertarpositiva);
            } else {
                JOptionPane.showMessageDialog(null, MensajeInsertarnegativa);
            }
        } else {
            JOptionPane.showMessageDialog(null, DatosNOValidos);
        }
    }//GEN-LAST:event_btnoIngresarActionPerformed

    private void btnoModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnoModificarActionPerformed
        boolean datosvalidos = true;
        String Id = cajaid.getText();
        String codigo = cajaPerito.getText();
        String Fruta = cajaIdFruta.getText();
        String Asunto = cajaAsunto.getText();
        String descr = cajaDescripcion.getText();

        if ("".equals(Id) || "".equals(Fruta) || "".equals(Asunto) || "".equals(descr)) {
            datosvalidos = false;
        }
        if (datosvalidos == true) {
            if (GestionInformes.modificar(Id, codigo, Fruta, Asunto, descr) == true) {
                JOptionPane.showMessageDialog(null, MensajeModificadopositiva);
            } else {
                JOptionPane.showMessageDialog(null, MensajeModificadonegativa);

            }
        } else {
            JOptionPane.showMessageDialog(null, DatosNOValidos);
        }
    }//GEN-LAST:event_btnoModificarActionPerformed

    private void btnoBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnoBorrarActionPerformed
        String Id = cajaid.getText();
        boolean datosvalidos = true;

        if ("".equals(Id)) {
            datosvalidos = false;
        }
        if (datosvalidos == true) {
            if (GestionInformes.borrar(Id) == true) {
                JOptionPane.showMessageDialog(null, Mensajeborrarpositiva);
                cajaid.setText("");
                cajaIdFruta.setText("");
                cajaAsunto.setText("");
                cajaDescripcion.setText("");
            } else {
                JOptionPane.showMessageDialog(null, Mensajeborrarnegativa);
            }
        } else {
            JOptionPane.showMessageDialog(null, DatosNOValidos);
        }
    }//GEN-LAST:event_btnoBorrarActionPerformed

