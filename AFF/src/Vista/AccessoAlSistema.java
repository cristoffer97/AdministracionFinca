    private void btnoIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnoIngresarActionPerformed
        boolean validos = true;
        String nombre = cajaNombre.getText();
        String valor = cajaCod.getText();
        try {
            int codigo = Integer.parseInt(valor);

            if ("".equals(nombre) && "".equals(valor)) {
                validos = false;
            }
            if (validos == true) {
                if (consultas.validardatos(nombre, codigo) == true) {
                    VentanaTareas vt = new VentanaTareas();
                    vt.setVisible(true);
                    dispose();

                } else {
                    JOptionPane.showMessageDialog(null, Datos_NO_VALIDOS,
                        TituloError, JOptionPane.ERROR_MESSAGE);                                        
                }
            } else {
                JOptionPane.showMessageDialog(null, Datos_NO_VALIDOS,
                        TituloError, JOptionPane.ERROR_MESSAGE);                    
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, Datos_NO_VALIDOS,
                        TituloError, JOptionPane.ERROR_MESSAGE);                    
        }

    }//GEN-LAST:event_btnoIngresarActionPerformed

