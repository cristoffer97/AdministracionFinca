    /**
     * Con este metodo el codigo introducido se registra la con la salida o
     * entrada del trabajo
     *
     * @param evt
     */
    private void botonFicharActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonFicharActionPerformed
        try {
            String cod = cajaCodigo.getText();

            int codigo = Integer.parseInt(cod);
            if (radioButonFechaEntrada.isSelected()) {
                if (consultas.existeTrabajador(codigo) == true) {
                    if (consultas.trabajadorDuplicado(codigo) == true) {
                        consultas.FicharTrabajadorEntrada(codigo);
                        cajaCodigo.setText("");
                    } else {
                        
                        JOptionPane.showMessageDialog(null, Fichaje_Duplicado,
                        TituloError, JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, Codigo_No_Registrado,
                        TituloError, JOptionPane.ERROR_MESSAGE);                    
                }
            } else if (radioButonFechaSalida.isSelected()) {
                consultas.FicharTrabajadorSalida(codigo);
                cajaCodigo.setText("");

            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, Codigo_No_Valido,
                        TituloError, JOptionPane.ERROR_MESSAGE);                    
         
        }


    }//GEN-LAST:event_botonFicharActionPerformed

